 
 $(document).ready(function(){
    $('#qu1').click(function(){
        const el = $('#an1');
    if(el.data('is-hidden') == true){
        el.show();
        el.data('is-hidden', false);
    } else {
        el.hide();
        el.data('is-hidden', true);
    }
});
});

